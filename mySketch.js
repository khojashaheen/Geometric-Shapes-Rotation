function createPattern(i,j,increment) {
	createStar(i,j,increment)
	createOctagon(i,j,increment)
	createMiddleStar(i,j,increment)
}

function createStar(i,j,increment) {
				//blue star
				noStroke();

				fill("rgb(136,6,100)")
				beginShape();
				vertex(i, j+3*increment);
				vertex((i+2*increment),(j+2*increment));
				vertex(i+3*increment,j);
				vertex(i+4*increment,j+2*increment);
				vertex(i+6*increment,j+3*increment);
				vertex(i+4*increment,j+4*increment);
				vertex(i+3*increment,j+6*increment)
				vertex(i+2*increment,j+4*increment)
				endShape(CLOSE);			
	
}


function createOctagon(i,j, increment) {
				noStroke();
				fill("rgb(181,139,212)")
				beginShape();
				vertex(i,j+3*increment)
				vertex(i+increment,j+increment)
				vertex(i+3*increment,j)
				vertex((i+2*increment),(j+2*increment));
				vertex(i,j+3*increment)
				vertex(i+increment,j+5*increment)
				vertex(i+3*increment,j+6*increment)
				vertex(i+2*increment,j+4*increment)
				endShape(CLOSE);
				beginShape();
				vertex(i+6*increment,j+3*increment)
				vertex(i+5*increment,j+5*increment)
				vertex(i+3*increment,j+6*increment)
				vertex(i+4*increment,j+4*increment)
				vertex(i+6*increment,j+3*increment)
				vertex(i+5*increment,j+increment)
				vertex(i+3*increment,j)
				vertex(i+4*increment,j+2*increment)
				endShape(CLOSE);

}


function createMiddleStar(i,j,increment) {

				fill("rgb(204,206,234)")
				beginShape();
				vertex(i, j+3*increment);
				vertex((i+1*increment),(j+5*increment));
				vertex(i+3*increment,j+6*increment);
				vertex(i, j+6*increment);
				endShape(CLOSE);
	
				beginShape();
				vertex(i, j);
				vertex(i+3*increment, j);
				vertex((i+1*increment),(j+increment));
				vertex(i, j+3*increment);
				endShape(CLOSE);
	
				beginShape();
				vertex(i+6*increment, j+3*increment);
				vertex((i+5*increment),(j+5*increment));
				vertex(i+3*increment,j+6*increment);
				vertex(i+6*increment, j+6*increment);
				endShape(CLOSE);
				
				beginShape();
				vertex(i+6*increment, j);
				vertex(i+3*increment, j);
				vertex((i+5*increment),(j+increment));
				vertex(i+6*increment, j+3*increment);
				endShape(CLOSE);
	

}
